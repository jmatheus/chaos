package br.chaos.model.entities;

public class ClasseArmadura {

	private int modificadorDestreza;
	private int bonusArmadura;
	private int bonusEscudo;
	private int armaduraNatural;
	private int modificadorVariado;
	
	// Getis e Setiiis
	public int getModificadorDestreza() {
		return modificadorDestreza;
	}
	public void setModificadorDestreza(int modificadorDestreza) {
		this.modificadorDestreza = modificadorDestreza;
	}
	public int getBonusArmadura() {
		return bonusArmadura;
	}
	public void setBonusArmadura(int bonusArmadura) {
		this.bonusArmadura = bonusArmadura;
	}
	public int getBonusEscudo() {
		return bonusEscudo;
	}
	public void setBonusEscudo(int bonusEscudo) {
		this.bonusEscudo = bonusEscudo;
	}
	public int getArmaduraNatural() {
		return armaduraNatural;
	}
	public void setArmaduraNatural(int armaduraNatural) {
		this.armaduraNatural = armaduraNatural;
	}
	public int getModificadorVariado() {
		return modificadorVariado;
	}
	public void setModificadorVariado(int modificadorVariado) {
		this.modificadorVariado = modificadorVariado;
	}
	
}
