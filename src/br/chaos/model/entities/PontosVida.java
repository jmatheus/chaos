package br.chaos.model.entities;

public class PontosVida {
	
	private int dadoVida;
	private int modContituicao;
	private int pvTotais;
	private int pvAtuais;
	
	
	//Getis e Setiiis
	public int getDadoVida() {
		return dadoVida;
	}
	public void setDadoVida(int dadoVida) {
		this.dadoVida = dadoVida;
	}
	public int getModContituicao() {
		return modContituicao;
	}
	public void setModContituicao(int modContituicao) {
		this.modContituicao = modContituicao;
	}
	public int getPvTotais() {
		return pvTotais;
	}
	public void setPvTotais(int pvTotais) {
		this.pvTotais = pvTotais;
	}
	public int getPvAtuais() {
		return pvAtuais;
	}
	public void setPvAtuais(int pvAtuais) {
		this.pvAtuais = pvAtuais;
	}
	

}
